package clans.followers;

/**
 * Every Follower has a TalentRing, which specifies what Talent they have.
 * Every Talent has a Value from 1 - 10, 1 is lowest, meaning training this Talent is the hardest.
 */
public class TalentRing {

    private int singleHandswordTalent;
    private int twoHandSwordTalent;
    private int bowTalent;
    private int singleHandMaceTalent;
    private int daggerTalent;
    private int elementalFireMagic;
    private int elementalWaterMagic;
    private int elementalWindMagic;
    private int elementalEarthMagic;
    private int statusMagic;
    private int whiteMagic;
    private int teaching;
    private int mining;
    private int smithingWeapons;
    private int smithingArmors;
    private int enchantment;
    private int alchemy;

    public int getSingleHandswordTalent() {
        return singleHandswordTalent;
    }

    public void setSingleHandswordTalent(int singleHandswordTalent) {
        this.singleHandswordTalent = singleHandswordTalent;
    }

    public int getTwoHandSwordTalent() {
        return twoHandSwordTalent;
    }

    public void setTwoHandSwordTalent(int twoHandSwordTalent) {
        this.twoHandSwordTalent = twoHandSwordTalent;
    }

    public int getBowTalent() {
        return bowTalent;
    }

    public void setBowTalent(int bowTalent) {
        this.bowTalent = bowTalent;
    }

    public int getSingleHandMaceTalent() {
        return singleHandMaceTalent;
    }

    public void setSingleHandMaceTalent(int singleHandMaceTalent) {
        this.singleHandMaceTalent = singleHandMaceTalent;
    }

    public int getDaggerTalent() {
        return daggerTalent;
    }

    public void setDaggerTalent(int daggerTalent) {
        this.daggerTalent = daggerTalent;
    }

    public int getElementalFireMagic() {
        return elementalFireMagic;
    }

    public void setElementalFireMagic(int elementalFireMagic) {
        this.elementalFireMagic = elementalFireMagic;
    }

    public int getElementalWaterMagic() {
        return elementalWaterMagic;
    }

    public void setElementalWaterMagic(int elementalWaterMagic) {
        this.elementalWaterMagic = elementalWaterMagic;
    }

    public int getElementalWindMagic() {
        return elementalWindMagic;
    }

    public void setElementalWindMagic(int elementalWindMagic) {
        this.elementalWindMagic = elementalWindMagic;
    }

    public int getElementalEarthMagic() {
        return elementalEarthMagic;
    }

    public void setElementalEarthMagic(int elementalEarthMagic) {
        this.elementalEarthMagic = elementalEarthMagic;
    }

    public int getStatusMagic() {
        return statusMagic;
    }

    public void setStatusMagic(int statusMagic) {
        this.statusMagic = statusMagic;
    }

    public int getWhiteMagic() {
        return whiteMagic;
    }

    public void setWhiteMagic(int whiteMagic) {
        this.whiteMagic = whiteMagic;
    }

    public int getTeaching() {
        return teaching;
    }

    public void setTeaching(int teaching) {
        this.teaching = teaching;
    }

    public int getMining() {
        return mining;
    }

    public void setMining(int mining) {
        this.mining = mining;
    }

    public int getSmithingWeapons() {
        return smithingWeapons;
    }

    public void setSmithingWeapons(int smithingWeapons) {
        this.smithingWeapons = smithingWeapons;
    }

    public int getSmithingArmors() {
        return smithingArmors;
    }

    public void setSmithingArmors(int smithingArmors) {
        this.smithingArmors = smithingArmors;
    }

    public int getEnchantment() {
        return enchantment;
    }

    public void setEnchantment(int enchantment) {
        this.enchantment = enchantment;
    }

    public int getAlchemy() {
        return alchemy;
    }

    public void setAlchemy(int alchemy) {
        this.alchemy = alchemy;
    }
}
