package clans.followers.skillpointring;

public class WhiteMagicSkillpointRing {
    int dormus_vitae; //heals a player
    int rex_res_mortem; //massive single- dmg only to undead
    int prismorum; //puts a shield on every player to take 1 hit

    public int getDormus_vitae() {
        return dormus_vitae;
    }

    public void setDormus_vitae(int dormus_vitae) {
        this.dormus_vitae = dormus_vitae;
    }

    public int getRex_res_mortem() {
        return rex_res_mortem;
    }

    public void setRex_res_mortem(int rex_res_mortem) {
        this.rex_res_mortem = rex_res_mortem;
    }

    public int getPrismorum() {
        return prismorum;
    }

    public void setPrismorum(int prismorum) {
        this.prismorum = prismorum;
    }
}
